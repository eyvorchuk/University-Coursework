/* Q1 */
select *
from Person
where age < 18;

/* Q2 */
select pizzeria, pizza, price
from eats natural join serves
where eats.name = 'Amy' and serves.price < 10;

/* Q3 */
select pizzeria, name, age
from Person natural join Frequents
where age < 18;

/* Q4 */
CREATE VIEW X as 
select pizzeria, name, age
from Person natural join Frequents
where age < 18;

CREATE VIEW Y as 
select pizzeria, name, age
from Person natural join Frequents
where age > 30;

select DISTINCT pizzeria
from X join Y using(pizzeria);

/* Q5 */
select pizzeria, X.name as Person1, X.age as age1, Y.name as Person2, Y.age as age2 
from X join Y using(pizzeria);

/* Q6 */
select name, count(pizza) as numTypes
from eats
group by name
having numTypes >= 2
order by numTypes desc;

/* Q7 */
select pizza, avg(price) as avgPrice
from serves
group by pizza
order by avgPrice desc;

